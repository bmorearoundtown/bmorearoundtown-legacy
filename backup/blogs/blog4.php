<h2 class="blog-title">Monday Morning Quarterback: Week 2</h2>

<p class="user-info">By Chris Caldwell, BMORE Around Town</p>

</div>

<div class="blog-content">

<p class="blog-text">

The Ravens absolutely dominated the Pittsburgh Steelers last Thursday on both sides of the ball for a much-needed victory. I was trying to convince myself that it wasn't considered a "must-win" game, but imagine starting 0-2, at home, against 2 division opponents... Nevertheless, the Ravens rallied around themselves and blocked out off the field distractions to take care of business in Baltimore. Here were the difference makers:

</p>

<div style="margin: 10px 0 10px 0;">

	<ol class="blog-list-ordered labels-list">
		
		<li>
		
			<p>
<strong>A play-making defense.</strong> The Ravens defense set the tone on the opening Steelers drive by absolutely CRUSHING Big Ben on a 3rd down pass play, only to get flagged for a questionable personal foul. Many plays later in the drive, Daryl Smith stripped the ball (in field goal range) to give the ball (deservingly) back to the Ravens with 0 points on the board. This veteran play was huge in keeping the crowd in the game early. Throw it another fumble recovery by Mosley and 2 sacks by Dumervil and the linebackers had a really nice night.
			</p>
			
		</li>

		<li>
		
			<p>
<strong>An effective running game.</strong> While the Ravens didn't necessary fill up the stat sheet, their running game was efficient, timely, and physical. Bernard Pierce looked very strong, and Forsett was the lightning to his thunder with a nice 41 yard scamper. Another affective runner was, wait for it... Joe Flacco. Joe successfully converted two 1-yard QB sneaks, gaining 2 yards on them both. You can tell he's worked on these (or just has a center now who can block someone). Also not to be overlooked, the Ravens won the time of possession, 35 min - 25 min. Mission Accomplished. 
			</p>
			
		</li>

		<li>

			<p>
<strong>The crowd/atmosphere of the game.</strong> Kudos to the crowd for sticking behind their team through all of the Ravens off-the-field issues over the last few weeks and keeping the intensity for this rivalry. M&T was loud and the Steelers were certainly rattled. I cannot remember the last time a Steelers/Ravens game wasn't even competitive in the 4th quarter. 
			</p>
			
		</li>
		
	</ol>
	
