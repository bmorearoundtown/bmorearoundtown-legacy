<h2 class="blog-title">Monday Morning Quarterback: Week 3</h2>

<p class="user-info">By Chris Caldwell, BMORE Around Town</p>

</div>

<div class="blog-content">

<p class="blog-text">

Let me first start off by saying that the city of Cleveland absolutely HATES the Ravens. We travel A LOT to opposing cities with fans to watch Ravens games, and this certainly made our top-5 worst fan-interaction list. We realize every fan base has their select A-holes (as I'm sure the Ravens do), but Cleveland's distaste for the Ravens was across the board. I'm surprised I did not even run across a toddler flipping us the bird. Enjoy wearing jerseys for a back-up frat boy QB. That looks like fun. Anyways, I digress. Let's get into the actual game re-cap:
</p>

<div style="margin: 10px 0 10px 0;">

	<ol class="blog-list-ordered labels-list">
		
		<li>
		
			<p>
<strong>Offensive Line Domination:</strong> The Browns had no answer to the power blocking/running style of KO, Yanda, and Taliaferro. The Ravens were shredding off 4-6 yard gains with ease and putting their offense in positions to succeed. So far, Kubiak's influence on the running game is the biggest difference we see from last year's team. We'll see if he sticks with Zo, or moves back to incumbent starter, Pierce this coming week. It's a storyline to watch.
			</p>
			
		</li>

		<li>
		
			<p>
<strong>Our secondary is BAD:</strong> It is inexcusable to get absolutely lit-up by Brian Hoyer, Andrew Hawkins, and Miles Austin. Matt Elam looked lost, dazed, and confused. Webb (understandably) was rusty. And the combo of Brown/Jackson don't deserve to be on the field for more than a handful of plays per game. The loss of Corey Graham turns out to be more significant than originally thought. Hopefully the addition of Will Hill after Week 6 will turn around this unit. It's going to be ugly when we play a real Quarterback. 
			</p>
			
		</li>

