<h2 class="blog-title">Monday Morning Quarterback: Week 1</h2>

<p class="user-info">By Chris Caldwell, BMORE Around Town</p>

</div>

<div class="blog-content">

<p class="blog-text">

	So much for "perfect in home-openers under Harbaugh." The Ravens were taken to school yesterday by the Bengals in their 23-16 loss. The game, honestly, wasn't nearly that close. Without a bonehead play by PacMan jones for an 80 yard Steve Smith TD, the Ravens didn't even stand a chance. Here were the difference makers:

</p>

<div style="margin: 10px 0 10px 0;">

	<ol class="blog-list-ordered labels-list">
		
		<li>
		
			<p>
				Did Suggs and Dumervil even dress to play the game? When you have two "difference-makers" at defensive end, it is inexcusable to have them both be SILENT the entire game. Dalton hardly ever had to move his feet before making a throw. While the D-Line did a good job shutting down the run, they have got to get more pass pressure. That being said, the secondary did a good job holding strong while the D-line attempted (poorly) to get after the QB.
			</p>
			
		</li>

		<li>
		
			<p>
			62 pass attempts. 7 of which, were dropped passes. Losing or not, you cannot throw the ball 62 times and expect to win. Kubiak's offense is built around zone-running and working off the play-action. I'd understand if it was a 3-4 TD game, but the game was never outside of 2 scores. Stick to your brand of offense. I love Joe Flacco, but he is not a gun-slinger.
			</p>
			
		</li>

		<li>

			<p>
			The crowd. It was a very "average" display from the home-crowd at M&T. Far from the seat-rattling cheers that we're used to when playing at home. The team will certainly need our back this Thursday against the Steelers.
			</p>
			
		</li>
		
	</ol>
	
	<div class="final-thoughts">
	
		<h3><u>Final Thoughts</u></h3>
		
		<p>
		As much as I hate to say it, Thursday might be a must-win game for the Ravens. Starting 0-2, at home, against the division, heading on the road, is a pretty tough hole to climb out of. It's Steelers week, let's get pumped!
		</p>
	
	</div>
	
